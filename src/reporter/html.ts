import type { Finding, ScanResult, Severity, CryptoCategory } from "../types.js";
import { SEVERITY_ORDER } from "../types.js";

const VERSION = "0.2.0";

const SEVERITY_COLORS: Record<Severity, string> = {
  critical: "#dc2626",
  high: "#ea580c",
  medium: "#ca8a04",
  low: "#2563eb",
  safe: "#16a34a",
};

const SEVERITY_BG: Record<Severity, string> = {
  critical: "#fef2f2",
  high: "#fff7ed",
  medium: "#fefce8",
  low: "#eff6ff",
  safe: "#f0fdf4",
};

const CATEGORY_LABELS: Record<CryptoCategory, string> = {
  kem: "KEM",
  signature: "Signature",
  hash: "Hash",
  symmetric: "Symmetric",
  protocol: "Protocol",
  kdf: "KDF",
};

/**
 * Generate a self-contained HTML report for pqaudit scan results.
 * No external dependencies -- all CSS is inlined.
 */
export function formatHtml(result: ScanResult): string {
  const { summary } = result;
  const safeCount = summary.bySeverity.safe ?? 0;
  const totalFindings = summary.findingsTotal;
  const pqcScore =
    totalFindings > 0 ? Math.round((safeCount / totalFindings) * 100) : 100;

  const sortedFindings = [...result.findings].sort(
    (a, b) => SEVERITY_ORDER[a.severity] - SEVERITY_ORDER[b.severity],
  );

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>pqaudit — Post-Quantum Readiness Report</title>
<style>
${css()}
</style>
</head>
<body>
<header class="header">
  <div class="container">
    <h1 class="header-title">pqaudit</h1>
    <p class="header-subtitle">Post-Quantum Readiness Report</p>
    <div class="header-meta">
      <span class="meta-item">Target: <code>${esc(result.target)}</code></span>
      <span class="meta-item">Scanned: ${esc(result.timestamp)}</span>
    </div>
    <div class="status-badge ${summary.pqcReady ? "status-ready" : "status-not-ready"}">
      ${summary.pqcReady ? "PQC READY" : "NOT PQC READY"}
    </div>
  </div>
</header>

<main class="container">
  ${renderDashboard(summary, pqcScore)}
  ${renderFindings(sortedFindings)}
</main>

<footer class="footer">
  <div class="container">
    <p>Generated by <a href="https://github.com/PQCWorld/pqaudit" rel="noopener noreferrer">pqaudit v${VERSION}</a></p>
    <p class="footer-timestamp">${esc(result.timestamp)}</p>
  </div>
</footer>
</body>
</html>`;
}

/* ---------- Section renderers ---------- */

function renderDashboard(
  summary: ScanResult["summary"],
  pqcScore: number,
): string {
  const severities: Severity[] = ["critical", "high", "medium", "low", "safe"];
  const categories = Object.keys(summary.byCategory) as CryptoCategory[];

  return `
<section class="dashboard">
  <h2 class="section-title">Summary</h2>
  <div class="dashboard-grid">
    <div class="card">
      <h3 class="card-title">Severity Breakdown</h3>
      <div class="badge-row">
${severities.map((s) => `        <span class="severity-badge" style="background:${SEVERITY_BG[s]};color:${SEVERITY_COLORS[s]};border:1px solid ${SEVERITY_COLORS[s]}20">${s}: ${summary.bySeverity[s]}</span>`).join("\n")}
      </div>
    </div>
    <div class="card">
      <h3 class="card-title">Category Breakdown</h3>
      <div class="badge-row">
${categories.map((c) => `        <span class="category-badge">${CATEGORY_LABELS[c] ?? c}: ${summary.byCategory[c]}</span>`).join("\n")}
      </div>
    </div>
    <div class="card stat-card">
      <div class="stat">
        <span class="stat-value">${summary.filesScanned}</span>
        <span class="stat-label">Files Scanned</span>
      </div>
      <div class="stat">
        <span class="stat-value">${summary.findingsTotal}</span>
        <span class="stat-label">Findings</span>
      </div>
      <div class="stat">
        <span class="stat-value" style="color:${pqcScore >= 80 ? "#16a34a" : pqcScore >= 50 ? "#ca8a04" : "#dc2626"}">${pqcScore}%</span>
        <span class="stat-label">PQC Score</span>
      </div>
    </div>
  </div>
</section>`;
}

function renderFindings(findings: Finding[]): string {
  if (findings.length === 0) {
    return `
<section class="findings">
  <h2 class="section-title">Findings</h2>
  <p class="no-findings">No findings detected. Your codebase appears post-quantum ready.</p>
</section>`;
  }

  return `
<section class="findings">
  <h2 class="section-title">Findings</h2>
  <div class="table-wrapper">
    <table class="findings-table">
      <thead>
        <tr>
          <th>Severity</th>
          <th>Algorithm</th>
          <th>Description</th>
          <th>File:Line</th>
          <th>Replacement</th>
          <th>Effort</th>
          <th>Confidence</th>
        </tr>
      </thead>
      <tbody>
${findings.map((f) => renderRow(f)).join("\n")}
      </tbody>
    </table>
  </div>
</section>`;
}

function renderRow(f: Finding): string {
  const loc = f.location.line
    ? `${f.location.file}:${f.location.line}`
    : f.location.file;
  const occ =
    f.occurrences && f.occurrences > 1
      ? ` <span class="occ-badge">${f.occurrences}x</span>`
      : "";
  const conf = `${Math.round(f.confidence * 100)}%`;

  return `        <tr class="severity-row-${f.severity}">
          <td><span class="severity-badge" style="background:${SEVERITY_BG[f.severity]};color:${SEVERITY_COLORS[f.severity]};border:1px solid ${SEVERITY_COLORS[f.severity]}20">${f.severity}</span></td>
          <td class="col-algo">${esc(f.algorithm)}${occ}</td>
          <td>${esc(f.description)}</td>
          <td class="col-file"><code>${esc(loc)}</code></td>
          <td>${f.replacement ? esc(f.replacement) : '<span class="na">--</span>'}</td>
          <td><span class="effort-badge effort-${f.effort}">${f.effort}</span></td>
          <td>${conf}</td>
        </tr>`;
}

